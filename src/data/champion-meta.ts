export const regions = Array.from(new Set(Object.values(championMeta).map((m) => m.region))).sort();
export const classes = Array.from(new Set(Object.values(championMeta).map((m) => m.class))).sort();
export const eras = ["2009-2012", "2013-2016", "2017-2020", "2021-2023", "2024-2025"];

export function getChampionMeta(name: string): ChampionMeta | undefined {
  return championMeta[name];
}

export function filterChampionsByMeta(
  pool: string[],
  filters: { region?: string; era?: string; class?: string }
): string[] {
  return pool.filter((name) => {
    const meta = championMeta[name];
    if (!meta) return true;
    if (filters.region && meta.region !== filters.region) return false;
    if (filters.class && meta.class !== filters.class) return false;
    if (filters.era) {
      const [start, end] = filters.era.split("-").map(Number);
      if (meta.releaseYear < start || meta.releaseYear > end) return false;
    }
    return true;
  });
}
